-- 1. user.email을 NOT NULL → NULL 허용으로 변경
--    (UNIQUE 제약은 그대로 유지 — MySQL은 NULL 여러 개를 unique 위반으로 보지 않음)
ALTER TABLE `user`
    MODIFY COLUMN `email` VARCHAR(255) NULL COMMENT '소셜 이메일 (카카오는 이메일 동의 항목 미사용 심사로 NULL 가능)';
